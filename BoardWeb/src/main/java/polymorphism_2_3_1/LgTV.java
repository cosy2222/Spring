package polymorphism_2_3_1;

public class LgTV {
	
	public void turnOn() {
		System.out.println("LgTV -- 전원을 켠다");
	}
	
	public void turnOff() {
		System.out.println("LgTV -- 전원을 끈다");
	}
	
	public void soundUP() {
		System.out.println("LgTV--소리 커짐");
	}
	
	public void soundDown() {
		System.out.println("LgTV--소리 작아짐");
	}

}
